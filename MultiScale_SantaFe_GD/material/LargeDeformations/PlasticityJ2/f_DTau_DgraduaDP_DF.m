function DPF = f_DTau_DgraduaDP_DF(strsg,dmatx,FInv2D,FInvzz)

   %Esta función fue sacada directamente de lo programado en fortran en la función f_RMapPlastJ2LD.f, para
   %verificación.

   %Como se utiliza en varias términos se calcula los cuadrados de los términos de FInv
   SqFInv11 = FInv2D(1,1)^2;
   SqFInv22 = FInv2D(2,2)^2;
   SqFInv33 = FInvzz^2;
   SqFInv12 = FInv2D(1,2)^2;
   SqFInv21 = FInv2D(2,1)^2;

   %Determinación de la derivada D[P]/D[F]
   %Componente 1111
   DPF(1,1) = SqFInv11*(dmatx(1,1)+strsg(1))+...
   SqFInv12*(dmatx(3,3)+strsg(2))+...
   FInv2D(1,1)*FInv2D(1,2)*(dmatx(1,3)+dmatx(3,1)+2*strsg(3));
   %Componente 2211
   DPF(2,1) = FInv2D(1,1)*(dmatx(3,1)*FInv2D(2,1)+...
   dmatx(2,1)*FInv2D(2,2))+...
   FInv2D(1,2)*(dmatx(3,3)*FInv2D(2,1)+dmatx(2,3)*FInv2D(2,2));
   %Componente 3311
   DPF(3,1) = (dmatx(4,1)*FInv2D(1,1)+dmatx(4,3)*FInv2D(1,2))*FInvzz;
   %Componente 1211
   DPF(4,1) = FInv2D(1,2)*(FInv2D(2,2)*(dmatx(3,3)+strsg(2))+...
   FInv2D(2,1)*(dmatx(1,3)+strsg(3)))+...
   FInv2D(1,1)*(FInv2D(2,1)*(dmatx(1,1)+strsg(1))+...
   FInv2D(2,2)*(dmatx(3,1)+strsg(3)));
   %Componente 2111
   DPF(5,1) = (dmatx(2,1)+dmatx(3,3))*FInv2D(1,1)*FInv2D(1,2)+...
   dmatx(3,1)*SqFInv11+dmatx(2,3)*SqFInv12;
   %Componente 1122
   DPF(1,2) = FInv2D(1,1)*(dmatx(1,3)*FInv2D(2,1)+...
   dmatx(1,2)*FInv2D(2,2))+...
   FInv2D(1,2)*(dmatx(3,3)*FInv2D(2,1)+...
   dmatx(3,2)*FInv2D(2,2));
   %Componente 2222
   DPF(2,2) = SqFInv21*(dmatx(3,3)+strsg(1))+...
   SqFInv22*(dmatx(2,2)+strsg(2))+...
   FInv2D(2,1)*FInv2D(2,2)*(dmatx(2,3)+dmatx(3,2)+2*strsg(3));
   %Componente 3322
   DPF(3,2) = (dmatx(4,3)*FInv2D(2,1)+dmatx(4,2)*FInv2D(2,2))*FInvzz;
   %Componente 1222
   DPF(4,2) = (dmatx(1,2)+dmatx(3,3))*FInv2D(2,1)*FInv2D(2,2)+...
   dmatx(1,3)*SqFInv21+dmatx(3,2)*SqFInv22;
   %Componente 2122
   DPF(5,2) = FInv2D(1,2)*(FInv2D(2,2)*(dmatx(2,2)+strsg(2))+...
   FInv2D(2,1)*(dmatx(2,3)+strsg(3)))+...
   FInv2D(1,1)*(FInv2D(2,1)*(dmatx(3,3)+strsg(1))+...
   FInv2D(2,2)*(dmatx(3,2)+strsg(3)));
   %Componente 1133
   DPF(1,3) = (dmatx(1,4)*FInv2D(1,1)+dmatx(3,4)*FInv2D(1,2))*FInvzz;
   %Componente 2233
   DPF(2,3) = (dmatx(3,4)*FInv2D(2,1)+dmatx(2,4)*FInv2D(2,2))*FInvzz;
   %Componente 3333
   DPF(3,3) = SqFInv33*(dmatx(4,4)+strsg(4));
   %Componente 1233
   DPF(4,3) = (dmatx(1,4)*FInv2D(2,1)+dmatx(3,4)*FInv2D(2,2))*FInvzz;
   %Componente 2133
   DPF(5,3) = (dmatx(3,4)*FInv2D(1,1)+dmatx(2,4)*FInv2D(1,2))*FInvzz;
   %Componente 1112
   DPF(1,4) = FInv2D(1,1)*(FInv2D(2,1)*(dmatx(1,1)+strsg(1))+...
   FInv2D(2,2)*(dmatx(1,3)+strsg(3)))+...
   FInv2D(1,2)*(FInv2D(2,2)*(dmatx(3,3)+strsg(2))+...
   FInv2D(2,1)*(dmatx(3,1)+strsg(3)));
   %Componente 2212
   DPF(2,4) = (dmatx(2,1)+dmatx(3,3))*FInv2D(2,1)*FInv2D(2,2)+...
   dmatx(3,1)*SqFInv21+...
   dmatx(2,3)*SqFInv22;
   %Componente 3312
   DPF(3,4) = (dmatx(4,1)*FInv2D(2,1)+dmatx(4,3)*FInv2D(2,2))*FInvzz;
   %Componente 1212
   DPF(4,4) = SqFInv21*(dmatx(1,1)+strsg(1))+...
   SqFInv22*(dmatx(3,3)+strsg(2))+...
   FInv2D(2,1)*FInv2D(2,2)*(dmatx(1,3)+dmatx(3,1)+2*strsg(3));
   %Componente 2112
   DPF(5,4) = FInv2D(1,2)*(dmatx(2,1)*FInv2D(2,1)+...
   dmatx(2,3)*FInv2D(2,2))+...
   FInv2D(1,1)*(dmatx(3,1)*FInv2D(2,1)+dmatx(3,3)*FInv2D(2,2));
   %Componente 1121
   DPF(1,5) = (dmatx(1,2)+dmatx(3,3))*FInv2D(1,1)*FInv2D(1,2)+...
   dmatx(1,3)*SqFInv11+dmatx(3,2)*SqFInv12;
   %Componente 2221
   DPF(2,5) = FInv2D(1,1)*(FInv2D(2,1)*(dmatx(3,3)+...
   strsg(1))+FInv2D(2,2)*(dmatx(2,3)+strsg(3)))+...
   FInv2D(1,2)*(FInv2D(2,2)*(dmatx(2,2)+strsg(2))+...
   FInv2D(2,1)*(dmatx(3,2)+strsg(3)));
   %Componente 3321
   DPF(3,5) = (dmatx(4,3)*FInv2D(1,1)+dmatx(4,2)*FInv2D(1,2))*FInvzz;
   %Componente 1221
   DPF(4,5) = FInv2D(1,2)*(dmatx(1,2)*FInv2D(2,1)+...
   dmatx(3,2)*FInv2D(2,2))+...
   FInv2D(1,1)*(dmatx(1,3)*FInv2D(2,1)+...
   dmatx(3,3)*FInv2D(2,2));
   %Componente 2121
   DPF(5,5) = SqFInv11*(dmatx(3,3)+strsg(1))+...
   SqFInv12*(dmatx(2,2)+strsg(2))+...
   FInv2D(1,1)*FInv2D(1,2)*(dmatx(2,3)+dmatx(3,2)+2*strsg(3));

end